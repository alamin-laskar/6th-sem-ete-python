    print("Ceil value: ",math.ceil(num))
    print("Exponantial value: ", math.exp(num))
    print("Abs of floating: ",math.fabs(num))
    print("Floor value: ",math.floor(num))
    print("log of :",math.log(num))
    print("log 2 of : ", math.log2(num))
    print("modf: ",math.modf(num))
    print("Power : ",math.pow(num,2))
    print("Square root: ",math.sqrt(num))


